package Polymorphism;

/**
 *
 * @author alex
 */
public class CompilePolymorphismSupp {
    
    /**
     * Adding two integer params
     * @param a
     * @param b
     * @return 
     */
    public int add(int a, int b) {
        return a+b;
    }
    
    /**
     * Adding three integer params
     * @param a
     * @param b
     * @param c
     * @return 
     */
    public int add (int a, int b, int c) {
        return a+b+c;
    }
    
}
